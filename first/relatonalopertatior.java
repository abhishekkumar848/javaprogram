public class relatonalopertatior {
    public static void main(String[] args) {
        // equal to equal ==
        int a= 10;
        int b=5;
        System.out.println(a==b);
        // not equal to !=
        int c= 10;
        int d=5;
        System.out.println(c!=d);
        // greater > 
        int e= 10;
        int f=5;
        System.out.println(e>f);
        // logical operater
                
        System.out.println((c!=d)&&(e>f));// && And operator
        System.out.println((c!=d)||(a==b));//  || or operator
        System.out.println(!(c!=d)); // ! not operator
    }
}
